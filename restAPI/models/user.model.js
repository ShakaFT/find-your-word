const database = require("mongoose")

const userSchema = database.Schema({
    email: {
        type: String,
        required: true,
        unique: true
    },
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    }
});

const User = database.model("users", userSchema)
module.exports = User
